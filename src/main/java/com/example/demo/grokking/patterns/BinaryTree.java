package com.example.demo.grokking.patterns;

public class BinaryTree {



	public static boolean isSymmetric(){

		
		
		return false;
	}


	public static class TreeNode {
		int val;
		TreeNode left;
		TreeNode right;

		TreeNode() {
		}

		TreeNode(int val) {
			this.val = val;
		}

		TreeNode(int val, TreeNode left, TreeNode right) {
			this.val = val;
			this.left = left;
			this.right = right;
		}
	}

}



